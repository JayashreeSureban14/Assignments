package typeCastingPackage;

public class Narrowing_ExplicitWay {

	public static void main(String[] args) {
		
		//converting double into int
		double d=86.06;
		int a = (int)d;
		System.out.println(a);
		
		//convert pi into int
		double pi=3.14;
		int newpi=(int)pi;
		System.out.println(newpi);

	}

}

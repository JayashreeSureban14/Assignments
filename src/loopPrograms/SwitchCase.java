package loopPrograms;

public class SwitchCase {

	public static void main(String[] args) {
		
		switch(10)
		{
		case 1:
			System.out.println("logic will be here");
			break;
			
		case 2:
			System.out.println("logic2 will be here");
			break;
			
		case 3:
			System.out.println("logic3 will be here");
			break;
			
		case 4:
			System.out.println("logic4 will be here");
			break;
			
			default : 
				System.out.println("default logic");
		}

	}

}

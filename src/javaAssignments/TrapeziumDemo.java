package javaAssignments;

public class TrapeziumDemo {

	public static void main(String[] args) {
		
		int base1=10;
		int base2=15;
		int height=12;
		
		double area=0.5*height*(base1+base2);
		
		System.out.println("Area of trapezium = "+area);
		
		int side1=10;
		int side2=9;
		int side3=15;
		int side4=11;
		
		int circum=side1+side2+side3+side4;
		
		System.out.println("Circumference of trapezium = "+circum);
		

	}

}
